CREATE TABLE RESULTS (
    ExpID INTEGER NOT NULL,
    SweepValues DOUBLE PRECISION,
    MeasValues DOUBLE PRECISION,
    CONSTRAINT fk_experiment FOREIGN KEY (ExpID)
		REFERENCES EXPERIMENT(ExpID)
);