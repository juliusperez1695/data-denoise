CREATE TABLE TESTER (
	OperID INTEGER PRIMARY KEY,
    FName VARCHAR(20),
    MidInit CHAR(1),
    LName VARCHAR(20),
    TeamRole VARCHAR(20)
);

CREATE TABLE EXPERIMENT (
	ExpID INTEGER PRIMARY KEY,
    ExpName VARCHAR(50),
    ExpDescr VARCHAR(300),
    SweepVar VARCHAR(20),
    MeasVar VARCHAR(20),
    Instrument VARCHAR(50)
);

CREATE TABLE DEVICE (
	LotID INTEGER NOT NULL,
    DeviceName VARCHAR(50) NOT NULL,
    DevDescr VARCHAR(300),
    TestByDate DATETIME,
    PRIMARY KEY (LotID, DeviceName)
);

